var canvas = new fabric.Canvas('myCanvas');

playerX = 10; //coordenada x inicial para o personagem como 10 e a armazena na variável (a imagem inicia à esquerda do canvas)
playerY = 10; //coordenada y inicial para o personagem como 10 e a armazena na variável (a imagem inicia no topo do cavas)

blockImageWidth = 30; //define a width inicial da imagem dos blocos como 30 e a armazena na variável
blockImageHeight = 30; //define a height inicial da imagem dos blocos como 30 e a armazena na variável

var playerObject= ""; //variável para armazenar o objeto da imagem do personagem
var blockImageObject= "";

function playerUpdate() //função para adicionar a imagem do personagem, enviando a imagem do jogador no canvas
//playerUpdate é o nome da função)
{
	fabric.Image.fromURL("player.png", function(Img) { //fabric é o nome da biblioteca que usamos, image diz que enviamos uma imagem
		//fromURL contém a URL da imagem e a função para enviar imagens, player.png é a imagem e function(Img) é a função que enviará
		//a imagem ao canvas
	playerObject = Img; //variável para armazenar o objeto padrão da imagem

	playerObject.scaleToWidth(150);
	playerObject.scaleToHeight(140);
	playerObject.set({
	top:playerY,
	left:playerX
	});
	canvas.add(playerObject);

	});
}

function newImage(getImage)
{
	fabric.Image.fromURL(getImage, function(Img) {
	blockImageObject = Img;

	blockImageObject.scaleToWidth(blockImageWidth);
	blockImageObject.scaleToHeight(blockImageHeight);
	blockImageObject.set({
	top:playerY,
	left:playerX
	});
	canvas.add(blockImageObject);

	});

}

